var Nun1, Nun2;

Nun1 = parseFloat(prompt("Digite o numero 1: "));
Nun2 = parseFloat(prompt("Digite o numero 2: "));

alert(`A soma de ${Nun1} e ${Nun2} é: ` + (Nun1 + Nun2));
alert(`A subtração de ${Nun1} e ${Nun2} é: ` + (Nun1 - Nun2));
alert(`A Multiplicação de ${Nun1} e ${Nun2}  é: ` + (Nun1 * Nun2));
alert(`A Divisão de ${Nun1} e ${Nun2} é: ` + (Nun1 / Nun2));
alert(`O resto de ${Nun1} e ${Nun2} é: ` + (Nun1 % Nun2));


